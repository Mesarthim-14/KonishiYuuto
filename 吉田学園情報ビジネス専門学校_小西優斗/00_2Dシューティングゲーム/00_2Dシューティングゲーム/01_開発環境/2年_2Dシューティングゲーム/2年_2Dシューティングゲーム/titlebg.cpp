#include "titlebg.h"
#include "renderer.h"
#include "manager.h"
#include "texture.h"

//====================================================================
// ポリゴン生成
//====================================================================
CTitleBg * CTitleBg::Create(D3DXVECTOR3 pos, D3DXVECTOR3 size, TYPE type)
{
	// インスタンス生成
	CTitleBg *pTitleBg = new CTitleBg;

	if (pTitleBg != NULL)
	{
		// 初期化処理
		pTitleBg->Init(pos, size, type);

		// テクスチャの設定
		pTitleBg->BindTexture(CTexture::GetTexture(CTexture::TEXTURE_NUM_TITLE));
	}

	return pTitleBg;
}

//====================================================================
// コンストラクタ
//====================================================================
CTitleBg::CTitleBg() : CScene2D(TYPE_BG)
{

}

//====================================================================
// デストラクタ
//====================================================================
CTitleBg::~CTitleBg()
{
}

//====================================================================
// 初期化処理
//====================================================================
HRESULT CTitleBg::Init(D3DXVECTOR3 pos, D3DXVECTOR3 size, TYPE type)
{
	// 初期化処理
	CScene2D::Init(pos, size, type);

	return S_OK;
}

//====================================================================
// 終了処理
//====================================================================
void CTitleBg::Uninit(void)
{
	// 終了処理
	CScene2D::Uninit();
}

//====================================================================
// 更新処理
//====================================================================
void CTitleBg::Update(void)
{
	// 更新処理
	CScene2D::Update();
}

//====================================================================
// 描画処理
//====================================================================
void CTitleBg::Draw(void)
{
	// 描画処理
	CScene2D::Draw();
}