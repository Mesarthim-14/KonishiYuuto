#include "resultbg.h"
#include "renderer.h"
#include "manager.h"
#include "texture.h"

//====================================================================
// ポリゴン生成
//====================================================================
CResultBg * CResultBg::Create(D3DXVECTOR3 pos, D3DXVECTOR3 size, TYPE type)
{
	// インスタンス生成
	CResultBg *pResultBg = new CResultBg;

	if (pResultBg != NULL)
	{
		// 初期化処理
		pResultBg->Init(pos, size, type);

		// テクスチャの設定
		pResultBg->BindTexture(CTexture::GetTexture(CTexture::TEXTURE_NUM_RESULT_BG));
	}

	return pResultBg;
}

//====================================================================
// コンストラクタ
//====================================================================
CResultBg::CResultBg() : CScene2D(TYPE_BG)
{
}

//====================================================================
// デストラクタ
//====================================================================
CResultBg::~CResultBg()
{
}

//====================================================================
// 初期化処理
//====================================================================
HRESULT CResultBg::Init(D3DXVECTOR3 pos, D3DXVECTOR3 size, TYPE type)
{
	// 初期化処理
	CScene2D::Init(pos, size, type);

	return S_OK;
}

//====================================================================
// 終了処理
//====================================================================
void CResultBg::Uninit(void)
{
	// 終了処理
	CScene2D::Uninit();
}

//====================================================================
// 更新処理
//====================================================================
void CResultBg::Update(void)
{
	// 更新処理
	CScene2D::Update();
}

//====================================================================
// 描画処理
//====================================================================
void CResultBg::Draw(void)
{
	// 描画処理
	CScene2D::Draw();
}