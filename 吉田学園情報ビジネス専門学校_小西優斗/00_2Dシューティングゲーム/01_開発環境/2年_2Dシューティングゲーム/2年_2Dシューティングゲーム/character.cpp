#include "character.h"
#include "renderer.h"
#include "manager.h"

//=============================================================================
// コンストラクタ
//=============================================================================
CCharacter::CCharacter(TYPE Priority):CScene2D(Priority)
{
	m_state = STATE_NONE;
	m_nLife = 0;
	m_nAttack = 0;
	m_nSpeed = 0;
}

//=============================================================================
// デストラクタ
//=============================================================================
CCharacter::~CCharacter()
{

}

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT CCharacter::Init(D3DXVECTOR3 pos, D3DXVECTOR3 size, TYPE type)
{
	// 初期化処理
	CScene2D::Init(pos, size, type);

	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void CCharacter::Uninit()
{
	// 終了処理
	CScene2D::Uninit();

	//オブジェクトの破棄
	Release();
}

//=============================================================================
// 更新処理
//=============================================================================
void CCharacter::Update()
{
	// 更新処理
	CScene2D::Update();
}

//=============================================================================
// 描画処理
//=============================================================================
void CCharacter::Draw()
{
	// 描画処理
	CScene2D::Draw();
}

//=============================================================================
// 体力の設定
//=============================================================================
void CCharacter::SetLife(int nLife)
{
	m_nLife = nLife;
}

//=============================================================================
// ダメージの処理
//=============================================================================
void CCharacter::HitDamage(int nLife)
{
	// ライフ減算
	m_nLife -= nLife;

	// ダメージ状態にする
	m_state = STATE_DAMAGE;

}

//=============================================================================
// 状態の設定
//=============================================================================
void CCharacter::SetState(STATE state)
{
	m_state = state;
}