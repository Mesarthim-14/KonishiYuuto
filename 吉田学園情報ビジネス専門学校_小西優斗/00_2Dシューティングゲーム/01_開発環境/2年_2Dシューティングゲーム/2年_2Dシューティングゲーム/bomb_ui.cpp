//=============================================================================

//====================================================================
// ポリゴン生成
//====================================================================
CBombUi * CBombUi::Create(D3DXVECTOR3 pos, D3DXVECTOR3 size, TYPE type)
{
	// インスタンス生成
	CBombUi *pBombUi = new CBombUi;

	if (pBombUi != NULL)
	{
		// 初期化処理
		pBombUi->Init(pos, size, type);

		// テクスチャの設定
		pBombUi->BindTexture(CTexture::GetTexture(CTexture::TEXTURE_NUM_BOMB_UI));
	}

	return pBombUi;
}

//====================================================================
// コンストラクタ
//====================================================================
CBombUi::CBombUi() : CScene2D(TYPE_BOMB)
{

}

//====================================================================
// デストラクタ
//====================================================================
CBombUi::~CBombUi()
{
}

//====================================================================
// 初期化処理
//====================================================================
HRESULT CBombUi::Init(D3DXVECTOR3 pos, D3DXVECTOR3 size, TYPE type)
{
	// 初期化処理
	CScene2D::Init(pos, size, type);

	return S_OK;
}

//====================================================================
// 終了処理
//====================================================================
void CBombUi::Uninit(void)
{
	// 終了処理
	CScene2D::Uninit();
}

//====================================================================
// 更新処理
//====================================================================
void CBombUi::Update(void)
{
	// 更新処理
	CScene2D::Update();
}

//====================================================================
// 描画処理
//====================================================================
void CBombUi::Draw(void)
{
	// 描画処理
	CScene2D::Draw();
}